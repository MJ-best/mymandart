  static List<String> getActionsForTheme(String theme) {
    // 테마에서 이모지와 키워드 추출
    final cleanTheme = theme.replaceAll(RegExp(r'[^\w\s가-힣]'), '').trim();

    // 각 키워드로 매칭 시도
    for (var keyword in cleanTheme.split(' ')) {
      if (keyword.isEmpty) continue;

      // 정확히 일치하는 키가 있는지 확인
      if (themeActions.containsKey(keyword)) {
        return themeActions[keyword]!;
      }

      // 부분 일치하는 키 찾기
      for (var key in themeActions.keys) {
        if (key.contains(keyword) || keyword.contains(key)) {
          return themeActions[key]!;
        }
      }
    }

    return actionExamples;
  }

